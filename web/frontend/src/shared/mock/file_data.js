export function GetData(start, end, sort) {


    if (!start.getTime) {
        start = new Date(start);
    }

    if (!end) {
        end = new Date();
    } else if (!start.getTime) {
        end = new Date(end);
    }

    var result = data.filter(f => f.timestamp >= start && f.timestamp < end);

    if (sort === "desc") {
        result = result.sort((a, b) => b.timestamp.getTime() - a.timestamp.getTime());
    }
    return result;
}

